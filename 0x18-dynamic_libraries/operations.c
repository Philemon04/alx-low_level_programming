#include "main.h"

/**
 *add - adds the two numbers
 * @a: first integer number
 * @b: second integer number
 * Return: Always 0(Success)
 **/
int add(int a, int b)
{
	return (a + b);
}

/**
 *  sub - subtracts two numbers
 *  @a: first integer number
 *  @b: second integer number
 *  Return: Always 0(Success)
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication of two numbers
 * @a: first integer number
 * @b: second integer number
 * Return: Always 0(Success)
 **/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two numbers
 * @a: first integer number
 * @b: second integer number
 * Return: Always 0(Success)
 **/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulo of two numbers
 * @a: first integer number
 * @b: second integer number
 * Return: Always 0(Success)
 **/

int mod(int a, int b)
{
	return (a % b);
}
